package Aula02;

public class TesteFor {
	public static void main(String args[]) {

		int num = 6;
		// int cont;
		for (int cont = 0; cont <= 10; cont++) {
			System.out.println(num + " x " + cont + " = " + (num * cont));
		}
	}
}
